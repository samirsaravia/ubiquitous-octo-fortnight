"""
Problem 7: Write a function count_digits to find number of digits in the given number.
Problema 7: Escreva uma função contar_digitos que encontre o numero de caracteres digitados
"""


def contar_digitos(escrita):
    comprimento = len(str(escrita))
    return comprimento


print(contar_digitos(12351231312))

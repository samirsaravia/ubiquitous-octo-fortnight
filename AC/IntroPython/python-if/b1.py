# if statement
valor = '6'

if valor == '7':
    # se for verdadeiro mostra esse print
    print('O valor é 7')
elif valor == '8':
    # se o anterior for falso e esse for verdadeiro mostra esse print
    print('O valor é 8')
elif valor == '9':
    print('O valor é 9')
else:
    # se for falso mostra esse print
    print('O valor procurado não foi encontrado')
print('Terminado')
